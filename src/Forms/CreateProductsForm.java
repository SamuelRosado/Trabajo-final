    //Boton para guardar o registrar nuevos productos.
    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        
        //Declaracion de variables.
                String nombre = txtname.getText();		
		String marca = txtbrand.getText();
		String categoria = txtcategory.getText();
		String precio = txtprice.getText();
		String cantidaddispo = txtamount.getText();
                
                //Instanciando la clase productos.
                Products products = new Products();
             
                //Dando valores a los campos.
                products.setNombre(nombre);		
		products.setMarca(marca);
		products.setCategoria(categoria);
		products.setPrecio(precio);
		products.setCantidad_Dispo(cantidaddispo);
                
                //Instancia la clase ProductsManage para aÃ±adir los productos a la base de datos.
                ProductsManage productmanage = new ProductsManage();
                
                int ko = productmanage.addproducts(products);
                // Condiciones
                if(ko >0 ) {
			JOptionPane.showMessageDialog(null, "Producto guardado correctamente.");   //Cuadro de dialogo
                        
                    this.dispose();  //Cierra formulario actual.
                    ProductsForm productform = new ProductsForm();
                    productform.setVisible(true);  //Llama al formulario de la tabla productos (ProductsForm).
                        
		}
		else {
			JOptionPane.showMessageDialog(null, "Error al guardar producto.","ERROR", JOptionPane.WARNING_MESSAGE); //Cuadro de dialogo
		}
    }//GEN-LAST:event_btnsaveActionPerformed

