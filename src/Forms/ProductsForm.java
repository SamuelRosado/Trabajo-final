    //Metodo para mostrar registros de la base de datos a la tabla productos
     void mostrarentablaproducto(){

        String sql = "select * from products";  //Query para mostrar en tabla productos (productstable)
        
        try {
            conected = conex.getconnect();  //Instanciando metodo de conexion de la base de datos (dbconection)
            state = conected.createStatement();
            rset = state.executeQuery(sql);  //Executando el query de la base de datos.
            Object[] data = new Object[5];   //Objeto de tipo arreglo o array.   
            model = (DefaultTableModel) producttable.getModel();
            
            //Condicion para mostrar los datos de la base de datos en la tabla productos (producttable)
            while(rset.next()){
                                       
                data [0] = rset.getString("Nombre");
                data [1] = rset.getString("Marca");
                data [2] = rset.getString("Categoria");
                data [3] = rset.getString("Precio");
                data [4] = rset.getString("Cantidad_Dispo");              
                model.addRow(data);
                
            }
            producttable.setModel(model);
        } catch (Exception e) {
        }
        
}

