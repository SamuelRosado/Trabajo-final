    private void producttableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_producttableMouseClicked
        
        
        int fila = producttable.getSelectedRow(); //Variable para el numero de filas
        
        //Condicion para detectar si no hay filas seleccionadas.
        if (fila == -1){
            
            JOptionPane.showMessageDialog(null, "No se selecciono ninguna fila");
        
        }else{
            this.dispose();   //Cierra formulario Actual
            EditProductsForm editproductform = new EditProductsForm();
            editproductform.setVisible(true);  //Llama el formulario editar o eliminar productos (EditProductsForm)
             
            //Declarion de variables y asignacion de posiciones 
           String nombre = (String) producttable.getValueAt(fila, 0);
           String marca = (String) producttable.getValueAt(fila, 1);
           String categoria = (String) producttable.getValueAt(fila, 2);
           String precio = (String) producttable.getValueAt(fila, 3);
           String cantidad = (String) producttable.getValueAt(fila, 4);
           
           //Asignando valores.
           EditProductsForm.txtname.setText(nombre);
           EditProductsForm.txtbrand.setText(marca);
           EditProductsForm.txtcategory.setText(categoria);
           EditProductsForm.txtprice.setText(precio);
           EditProductsForm.txtamount.setText(cantidad);
                            
        }
        
             
    }//GEN-LAST:event_producttableMouseClicked

